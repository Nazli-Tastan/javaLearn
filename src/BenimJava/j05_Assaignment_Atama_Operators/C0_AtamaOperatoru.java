public class C0_AtamaOperatoru {
    public static void main(String[] args) {

        //    =	        x = 8	    x = 8
        //    +=	    x += 3	    x = x + 3
        //    -=	    x -= 3	    x = x - 3
        //    *=	    x *= 3	    x = x * 3
        //    /=	    x /= 3	    x = x / 3
        //    %=	    x %= 3	    x = x % 3


        int yas=33;
        System.out.println(yas+5);//38 atama olmadığı için yas 33 hala

        System.out.println("yas="+yas);//yas33

        System.out.println(yas+=11);//atama yapıldığı için yaş artık 44





    }
}
