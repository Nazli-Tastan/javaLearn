package day01;

public class C04_SwitcCase {
    public static void main(String[] args) {

        String day = "Sunday";
        switch (day) {
            case "Monday":
            case "Tuesday":
                System.out.println("Java class");
                break;
            case "Thursday":
            case "Friday":
                System.out.println("Selenium class");
                break;
            case "Wednesday":
            case "Saturday":
                System.out.println("SQL class");
                break;
            default:
                System.out.println("day off");












        }






        }






    }

