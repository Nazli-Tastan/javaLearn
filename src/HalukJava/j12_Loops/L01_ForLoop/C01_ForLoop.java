package HalukJava.j12_Loops.L01_ForLoop;

public class C01_ForLoop {
    public static void main(String[] args) {


        //41 kere masallah
        System.out.println("masallah");
        System.out.println("masallah");
        System.out.println("masallah");
        System.out.println("masallahd");
        System.out.println("masallahd");
        System.out.println("masallahd");


        //loop döngü tekrarı için yazılır
        for (int i=1;i<=41; i++){

            System.out.println("masallah");
        }

        //2 basamaklı tek sayıları yazan bir code create ediniz

        for (int i = 11; i<=99; i+=2){
            System.out.println(i);
        }
        for (int i=10; i<100; i++){
            if (i %2==1){
                System.out.print(i+" ");
            }
        }
        for (int i=0; i>10 ; i++){
            System.out.print("salam javacanlara");
        }
        System.out.print("eeeeee");







    }
}
